# ── Language selector ─────────────────────────────────────────
select_language() {
  echo ""
  echo -e "${BOLD}Select language / 選擇語言 / 言語を選択 / Sprache wählen:${NC}"
  echo "  1) English"
  echo "  2) 繁體中文"
  echo "  3) 简体中文"
  echo "  4) 日本語"
  echo "  5) Deutsch"
  echo "  6) Español"
  echo "  7) Français"
  echo "  8) Türkçe"
  echo "  9) Українська"
  echo ""
  local choice
  while true; do
    read -rp "  [1-9]: " choice
    case "$choice" in
      1) GB10_LANG=en    ; break ;;
      2) GB10_LANG=zh-TW ; break ;;
      3) GB10_LANG=zh-CN ; break ;;
      4) GB10_LANG=ja    ; break ;;
      5) GB10_LANG=de    ; break ;;
      6) GB10_LANG=es    ; break ;;
      7) GB10_LANG=fr    ; break ;;
      8) GB10_LANG=tr    ; break ;;
      9) GB10_LANG=uk    ; break ;;
      *) echo "  Invalid / 無效 / 無効 — enter 1-9" ;;
    esac
  done
  export GB10_LANG
}

