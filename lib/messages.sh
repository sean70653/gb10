# ── Public API ────────────────────────────────────────────────
msg() {
  local key="$1"; shift
  local template="${_MSG[$key]:-$key}"
  if [ $# -gt 0 ]; then
    # shellcheck disable=SC2059
    printf "$template" "$@"
  else
    echo -e "$template"
  fi
}

info()  { echo -e "${CYAN}[INFO]${NC}  $(msg "$@")"; }
ok()    { echo -e "${GREEN}[ OK ]${NC}  $(msg "$@")"; }
warn()  { echo -e "${YELLOW}[WARN]${NC}  $(msg "$@")"; }
fail()  { echo -e "${RED}[FAIL]${NC}  $(msg "$@")"; exit 1; }

ask_yn() {
  local prompt="$1"
  local yn
  read -rp "  $prompt $(msg yes_no) " yn
  case "$yn" in
    [yYjJoOsSeEтТ]*) return 0 ;;
    *) return 1 ;;
  esac
}

